package Logical_Programs;

public class Star_Pattern {

	public static void main(String[] args) {
		
		//int space=5;
		for(int i=0; i<5; i++)//row
		{

		 for(int j=0; j<5; j++)//column
		{
			 System.out.print("*");
		}
		  System.out.println();
	}
		
		System.out.println("*************");
		
		for(int i=0; i<5; i++)//row
		{

		 for(int j=0; j<5; j++)//column
		{
			 System.out.print(j);
		}
		  System.out.println();
	}
		

  }

}
