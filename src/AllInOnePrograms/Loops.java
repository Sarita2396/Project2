package AllInOnePrograms;

public class Loops {

	public static void main(String[] args) {

		for(int i=0; i<=5; i++)
		{
			System.out.println(i);
		}
		
		int i=20;
		while(i<=5)
		{
			System.out.println(i);
			i++;
		}
		
		int j=30;
		do
		{
		System.out.println(j);	
		j++;
		}while(j<=5);
	}

}
