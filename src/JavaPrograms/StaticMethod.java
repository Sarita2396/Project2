package JavaPrograms;

public class StaticMethod {
	
	public static void display()
	{
		System.out.println("Hi Sarita");
	}
	
	public static void call()
	{
		System.out.println("Hello Sarita");
	}

	//To call static method in different class we can called it by classname.methodname
	//To call static method in same class we can called it by methodname only
}
