package JavaPrograms;

public class Loops {

	public static void main(String[] args) {

		for(int i=0; i<=5; i++)
		{
			System.out.println(i);
		}
		
		System.out.println("********************");
		
		int i=0;
		while(i<=5)
		{
			System.out.println(i);
			i++;
		}
		
		System.out.println("************************");
		
		int j=1;
		do
		{
			System.out.println(j);
			j++;
		}
		while(j<=5);
	}
	

}
