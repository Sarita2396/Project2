package JavaPrograms;

public class Casting {

	public static void main(String[] args) {

		int a = 10;
		float b = a;
		System.out.println(b);
		
		float f = 34.5f;
		int c = (int) f;
		System.out.println(c);
		
		int x = 80;
		double t = x;
		System.out.println(t);
		
		double s = 40;
		int g = (int) s;
		System.out.println(g);
		
	}

}
